#!/usr/bin/env ruby

puts "\n"

puts "====================="
puts "[ Little Ruby Codes ]"
puts "====================="

puts "\n"

puts "Once upon a time there lived in a certain village a little country girl, the prettiest creature who was ever seen. Her mother was excessively fond of her; and her grandmother doted on her still more. This good woman had a little red riding hood made for her. It suited the girl so extremely well that everybody called her Little Ruby."

puts "\n"

puts "One day her mother, having made some cakes, said to her, \"Go, my dear, and see how your grandmother is doing, for I hear she has been very ill. Take her a cake, and this little pot of butter.\""

puts "\n"

puts "Little Ruby set out immediately to go to her grandmother, who lived in another village."

puts "\n"

puts "As she was going through the wood, she met with a wolf, who had a very great mind to eat her up, but he dared not, because of some woodcutters working nearby in the forest. He asked her where she was going. The poor child, who did not know that it was dangerous to stay and talk to a wolf, said to him, \"I am going to see my grandmother and carry her a cake and a little pot of butter from my mother.\""

puts "\n"

puts "\"Does she live far off?\" said the wolf"

puts "\n"

puts "\"Oh I say,\" answered Little Ruby; \"it is beyond that mill you see there, at the first house in the village.\""

puts "\n"

puts "\"Well,\" said the wolf, \"and I'll go and see her too. I'll go this way and go you that, and we shall see who will be there first.\""

puts "\n"

puts "The wolf ran as fast as he could, taking the shortest path, and the little girl took a roundabout way, entertaining herself by gathering nuts, running after butterflies, and gathering bouquets of little flowers. It was not long before the wolf arrived at the old woman's house. He knocked at the door: tap, tap."

puts "\n"

puts "\"Who's there?\""

puts "\n"

puts "\"Your grandchild, Little Ruby,\" replied the wolf, counterfeiting her voice; \"who has brought you a cake and a little pot of butter sent you by mother.\""

puts "\n"

puts "The good grandmother, who was in bed, because she was somewhat ill, cried out, \"Pull the bobbin, and the latch will go up.\""

puts "\n"

puts "The wolf pulled the bobbin, and the door opened, and then he immediately fell upon the good woman and ate her up in a moment, for it been more than three days since he had eaten. He then shut the door and got into the grandmother's bed, expecting Little Ruby, who came some time afterwards and knocked at the door: tap, tap."

puts "\n"

puts "Who's there?"

puts "\n"

puts "Little Ruby, hearing the big voice of the wolf, was at first afraid; but believing her grandmother had a cold and was hoarse, answered, \"It is your grandchild Little Ruby, who has brought you a cake and a little pot of butter mother sends you.\""

puts "\n"

puts "The wolf cried out to her, softening his voice as much as he could, \"Pull the bobbin, and the latch will go up.\""

puts "\n"

puts "Little Ruby pulled the bobbin, and the door opened."

puts "\n"

puts "The wolf, seeing her come in, said to her, hiding himself under the bedclothes, \"Put the cake and the little pot of butter upon the stool, and come get into bed with me.\""

puts "\n"

puts "Little Ruby took off her clothes and got into bed. She was greatly amazed to see how her grandmother looked in her nightclothes, and said to her, \"Grandmother, what big arms you have!\""

puts "\n"

puts "\"All the better to hug you with, my dear.\""

puts "\n"

puts "\"Grandmother, what big legs you have!\""

puts "\n"

puts "\"All the better to run with, my child.\""

puts "\n"

puts "\"Grandmother, what big ears you have!\""

puts "\n"

puts "\"All the better to hear with, my child.\""

puts "\n"

puts "\"Grandmother, what big eyes you have!\""

puts "\n"

puts "\"All the better to see with, my child.\""

puts "\n"

puts "\"Grandmother, what big teeth you have got!\""

puts "\n"

puts "\"All the better to eat you up with.\""

puts "\n"

puts "And, saying these words, this wicked wolf fell upon Little Ruby, and ate her all up."

puts "\n"
puts "~~~~~~ THE END ~~~~~~"
